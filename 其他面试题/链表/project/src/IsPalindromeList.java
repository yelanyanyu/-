    //O(N)
    public static boolean isPalindrome1(Node head) {
        Stack<Node> stack = new Stack<>();
        Node cur = head;
        while (cur != null) {
            stack.push(cur);
            cur = cur.next;
        }
        cur = head;
        while (cur != null) {
            if (cur.value != stack.pop().value) {
                return false;
            }
            cur = cur.next;
        }
        return true;
    }


    //O(1)
    public static boolean isPalindrome3(Node head) {
        if (head == null) {
            return true;
        }
        if (head.next == null) {
            return true;
        }
        Node n1 = head;
        Node n2 = head;
        //注意循环条件的顺序
        while (n2.next != null && n2.next.next != null) {
            n1 = n1.next;
            n2 = n2.next.next;
        }
        n2 = n1.next;
        n1.next = null;
        Node n3 = reverseList(n2);//n3为逆转链表的头节点
        Node n5 = n3;
        Node n4 = head;
        boolean res = true;
        while (n3 != null && n4 != null) {
            if (n3.value != n4.value) {
                res = false;
                break;
            }
            n3 = n3.next;
            n4 = n4.next;
        }
        //恢复链表
        Node n6 = reverseList(n5);
        n1.next = n6;
        return res;
    }

    public static Node reverseList(Node head) {
        Node pre = null;
        Node next = null;
        while (head != null) {
            next = head.next;
            head.next = pre;
            pre = head;
            head = next;
        }
        return pre;
    }


